    [MenuItem("Tools/Generate ASCII Atlas")]
    public static void Generate()
    {
        int atlasRows = Mathf.CeilToInt(k_CharCount / (float)k_AtlasCols);
        int atlasW    = k_AtlasCols * k_CellW;
        int atlasH    = atlasRows   * k_CellH;

        Texture2D atlas = new Texture2D(atlasW, atlasH, TextureFormat.RGBA32, false);
        atlas.filterMode = FilterMode.Point;

        Color[] pixels = new Color[atlasW * atlasH];
        for (int i = 0; i < pixels.Length; i++) pixels[i] = Color.black;

        for (int i = 0; i < k_CharCount; i++)
        {
            int col   = i % k_AtlasCols;
            // Texture2D Y=0 is bottom; row 0 chars go at top → highest Y
            int row   = i / k_AtlasCols;
            int baseX = col * k_CellW;
            int baseY = (atlasRows - 1 - row) * k_CellH;

            for (int py = 0; py < k_CellH; py++)
            {
                byte rowBits = k_Font[i * k_CellH + py];
                // py=0 is top of glyph → highest Y in texture
                int texY = baseY + (k_CellH - 1 - py);
                for (int px = 0; px < k_CellW; px++)
                {
                    // MSB = leftmost pixel
                    bool lit = ((rowBits >> (7 - px)) & 1) == 1;
                    pixels[texY * atlasW + baseX + px] = lit ? Color.white : Color.black;
                }
            }
        }

        atlas.SetPixels(pixels);
        atlas.Apply();

        string fullPath = Path.Combine(Application.dataPath,
            k_SavePath.Substring("Assets/".Length));
        Directory.CreateDirectory(Path.GetDirectoryName(fullPath));
        File.WriteAllBytes(fullPath, atlas.EncodeToPNG());
        Object.DestroyImmediate(atlas);

        AssetDatabase.Refresh();

        TextureImporter ti = AssetImporter.GetAtPath(k_SavePath) as TextureImporter;
        if (ti != null)
        {
            ti.filterMode             = FilterMode.Point;
            ti.mipmapEnabled          = false;
            ti.textureCompression     = TextureImporterCompression.Uncompressed;
            ti.alphaIsTransparency    = false;
            ti.isReadable             = false;
            ti.npotScale              = TextureImporterNPOTScale.None;
            ti.sRGBTexture            = false;
            ti.textureType            = TextureImporterType.Default;
            ti.SaveAndReimport();
        }

        Debug.Log($"ASCII Atlas saved: {k_SavePath} ({atlasW}x{atlasH}px, " +
                  $"{k_CharCount} chars, {k_CellW}x{k_CellH}px each, CP437 bitmap font)");
    }
